@app.route('/drinks', methods=['POST'])
@requires_auth('post:drinks')
def create_drink():
    body = request.get_json()
    drink_title = body.get('title')
    drink_recipe = body.get('recipe')
    new_drink = Drink(title=drink_title, recipe=drink_recipe)

    new_drink.insert()

    drinking = [drink.long() for drink in new_drink]
    return jsonify({
        'status': 200,
        'success': True,
        'drinks': drinking
    })
'''
@TODO implement endpoint
    PATCH /drinks/<id>
        where <id> is the existing model id
        it should respond with a 404 error if <id> is not found
        it should update the corresponding row for <id>
        it should require the 'patch:drinks' permission
        it should contain the drink.long() data representation
    returns status code 200 and json {"success": True, "drinks": drink} where drink an array containing only the updated drink
        or appropriate status code indicating reason for failure
'''
@app.route('/drinks/<int:id>', methods=['PATCH'])
@requires_auth('patch:drinks')
def update_drink(id):
    drink = Drink.query.get_or_404(id)
    if drink not in Drink:
        abort(404)
        
    body = request.get_json()
    drink.title = body.get('title')
    drink.recipe = body.get('recipe')
    drink.update()

    updated_drink = [dr.long() for dr in drink]

    return jsonify({
        'status_code': 200,
        'success': True,
        'drinks': updated_drink
    })
